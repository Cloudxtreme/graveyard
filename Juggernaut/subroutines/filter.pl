sub filter {
	# Get variables from the shift.
	my $msg = shift;

	# Step 1: Substitutions. Let's make some common spelling corrections.
	&substitutions ($msg);

	# Step 2: Make the message be purely alphanumeric.
	$msg =~ s/[^A-Za-z0-9 ]//ig;

	# Return the message.
	return $msg;
}
1;