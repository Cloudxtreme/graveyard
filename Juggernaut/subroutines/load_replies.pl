sub load_replies {
	# Get variables from the shift.
	my ($bot,$brain,$dir) = @_;

	$brain = lc($brain);
	$brain =~ s/ //g;

	# See if there is a loading handler for this brain.
	# If not, skip it.
	my $load = $brain . '_load';
	if (-e "./brains/$load\.pl") {
		&{$load} ($bot,$brain,$dir);
	}

	return 1;
}
