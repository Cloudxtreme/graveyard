sub irc_disconnect {
	# Get variables from the server.
	my ($self,$event) = @_;

	my $time = localtime();

	print "ChaosIRC: Disconnected from " . $event->from() . " ("
		. ($event->args())[0] . ").\n"
		. "ChaosIRC: Attemtping to reconnect...\n";
	$self->connect();
}
